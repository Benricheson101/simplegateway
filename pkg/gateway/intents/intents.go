	// - MESSAGE_REACTION_REMOVE
	// - MESSAGE_REACTION_REMOVE_ALL
	// - MESSAGE_REACTION_REMOVE_EMOJI
	GUILD_MESSAGE_REACTIONS = 1 << 10

